                          The Higher Education Act of 1966 established a National Council of Higher Education (NCHE) and later in 1972 under the University of Sri Lanka Act No. 1 of 1972 all universities were brought under one umbrella and made Campuses of a single university established as the University of Sri Lanka. 
                          The University of Ceylon, Colombo was named the Colombo Campus of the University of Sri Lanka. This system prevailed until 1977. University autonomy was weakened and as a result, a new Act was introduced in 1978. Under the Universities Act No. 16 of 1978 all Campuses of the then single University became independent Universities.
                           Accordingly, the University of Colombo, Sri Lanka regained its autonomy in 1978.It had Faculties of Medicine, Arts, Science, Education, and Law. A Faculty of Graduate Studies was created by an Ordinance of the UGC in 1987, to further strengthen post-graduate education. Subsequently, the Faculty of Management and Finance was established. 
                         </br></br>
                          The Sri Palee Campus of the University was established by a Gazette notification in 1996. Institutes with financial autonomy were created by the earlier Act of 1972. When the single university was dissolved in 1978, the Postgraduate Institute of Medicine, the Institute of Workers’ Education, and the Institute of Indigenous medicine were affiliated to the University of Colombo.
                          The Institute of Computer Technology (ICT) was established in 1987. The University now has 9 Faculties with 56 Academic Departments, a Campus, a School, 6 Institutes, and 5 centres.</p>
